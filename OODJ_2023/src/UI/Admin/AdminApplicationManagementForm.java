    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(null,
                "After approving this application, this slot will be not available for 3 months.", "Are you sure you wanted to approve this application?",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE);
        switch (res) {
            case 0:
                // update Application object
                application.setApplicationStatus(ApplicationStatus.APPROVED);
                int capacity = application.getRoom().getCapacity();
                capacity = - 1;
                if (capacity == 0) {
                    application.getRoom().setIsAvailable(false);
                }
                ApplicationController.ActivateApplicationController().updateApplication(application);

                // create reservation record
                int reservationID = ReservationController.ActivateReservationController().getNewID();
                Reservation reservation = new Reservation(reservationID, application, LocalDateTime.now().plus(7, ChronoUnit.DAYS), LocalDateTime.now().plus(7, ChronoUnit.DAYS).plus(12, ChronoUnit.WEEKS), 12);
                ReservationController.ActivateReservationController().add(reservation);

                JOptionPane.showMessageDialog(this, "New reservation record is created.", "Application is Approved!", JOptionPane.INFORMATION_MESSAGE);
                this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
                
                AdminApplicationManagement.activateUI().resetApplications();
                break;
            case 1:
                break;
            default:
                break;
        }
    }//GEN-LAST:event_approveBtnActionPerformed

    private void rejectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectBtnActionPerformed
        // TODO add your handling code here:
        String reason = JOptionPane.showInputDialog(this, "Please write down the reason of rejecting this application.");
        this.application.setRejectedReason(reason);
        this.application.setApplicationStatus(ApplicationStatus.REJECTED);
        ApplicationController.ActivateApplicationController().updateApplication(application);
        AdminApplicationManagement.activateUI().resetApplications();
        JOptionPane.showMessageDialog(this, "The application is rejected...", "Application is Rejected!", JOptionPane.INFORMATION_MESSAGE);
        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_rejectBtnActionPerformed

