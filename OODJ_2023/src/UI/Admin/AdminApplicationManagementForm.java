    public void setApplication(Application ap) {
        this.application = ap;
        DateTimeFormatter format = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
        appIDLabel.setText(application.getApplicationID() + "");
        dateTimeLabel.setText(application.getDateTime().format(format));
        roomIDLabel.setText(application.getRoom().getRoomID() + "");
        roomTypeLabel.setText(application.getRoom().getClass().getSimpleName());
        String avai = "Available";
        if (!application.getRoom().isAvailable()) {
            avai = "Occupied";
        }
        availabilityLabel.setText(avai);
        capacityLabel.setText(application.getRoom().getCapacity() + "");
        amountLabel.setText(application.getPayment().getAmount() + "");
        studentUNLabel.setText(application.getStudent().getUsername());
        nicLabel.setText(application.getStudent().getNIC());
        status.setText(application.getApplicationStatus() + "");
        rejectedReason.setText(application.getRejectedReason());
        
        if(application.getApplicationStatus() != ApplicationStatus.PENDING){
            approveBtn.setVisible(false);
            rejectBtn.setVisible(false);
            cancelBtn.setVisible(false);
        }
    }

